#include "fillit.h"

char	***reset_tab(char ***tab, char ***tmp, int i)
{
	int j;

	j = 0;
	while (j < i)
	{
		if (!(tab[j]))
			tab[j] = tmp[j];
		j++;
	}
	return (tab);
}
